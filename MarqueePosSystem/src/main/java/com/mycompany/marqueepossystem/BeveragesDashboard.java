    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        if (removing) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int rowCount = model.getRowCount();
            if (rowCount > 0) {
                String totalValueStr = model.getValueAt(rowCount - 1, 3).toString().replace("₱", "").replace(",", "").trim();            
                double totalValue = Double.parseDouble(totalValueStr);
                ShareTableModel.totalAmount -= totalValue;
                model.removeRow(rowCount - 1);
                totalAmountTextField.setText("₱" + String.format("%.2f", ShareTableModel.totalAmount));
            }
        } else {
            JOptionPane.showMessageDialog(null, "Admim Void Required.");
        }
    }//GEN-LAST:event_removeButtonActionPerformed

