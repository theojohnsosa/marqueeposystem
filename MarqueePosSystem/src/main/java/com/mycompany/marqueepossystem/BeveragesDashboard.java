    private void addButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButton1ActionPerformed
        String beverageTitle = "Coca-Cola";
        double beveragePrice = 30.00;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        boolean found = false;
        for (int i = 0; i < model.getRowCount(); i++) {
            String existing = model.getValueAt(i, 0).toString();
            if (existing.equals(beverageTitle)) {
                int quantity = Integer.parseInt(model.getValueAt(i, 1).toString());
                quantity++;
                double newTotal = quantity * beveragePrice;
                model.setValueAt(quantity, i, 1);
                model.setValueAt("₱" + String.format("%.2f", newTotal), i, 3);
                ShareTableModel.totalAmount += beveragePrice;
                totalAmountTextField.setText("₱" + String.format("%.2f", ShareTableModel.totalAmount));
                found = true;
                break;
            }
        }

        if (!found) {
            model.addRow(new Object[]{beverageTitle, 1, "₱" + String.format("%.2f", beveragePrice), "₱" + String.format("%.2f", beveragePrice)});
            ShareTableModel.totalAmount += beveragePrice;
            totalAmountTextField.setText("₱" + String.format("%.2f", ShareTableModel.totalAmount));
        }
    }//GEN-LAST:event_addButton1ActionPerformed

